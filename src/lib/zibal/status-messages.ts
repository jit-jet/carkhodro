export function zibalStatusMessage(status: number | null | undefined): string {
  if (status == null || Number.isNaN(status)) {
    return 'پرداخت شما تکمیل نشد. لطفاً دوباره تلاش کنید.';
  }
  return STATUS_MESSAGES[status] ?? 'پرداخت شما تکمیل نشد. لطفاً دوباره تلاش کنید.';
}
